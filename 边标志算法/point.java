public class point {
    //x轴坐标，y轴坐标，index为点的序号
    int x,y;

    public point(int x, int y) {
        this.x = x;
        this.y = y;

    }
    public point(){}

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
